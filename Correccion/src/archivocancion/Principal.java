    private void btnabrirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnabrirActionPerformed
       String texto; //Variable para guardar la lectura
       txtmostrar.setText(""); //Limpiamos el JtextArea antes de comenzar
       try{
           //declaramos la variable archivo como un objeto File
           //y le asignamos una ruta donde se creara
           File archivo = new File(directorio+"/"+nombreArchivoTexto);
           //BufferedReader ayuda a manejar las lecturas de streams
           //FileReader mos ayuda a mirar dentro del archivo
           BufferedReader br=new BufferedReader(new FileReader(archivo));
           //verificamos dentro del archivo hasta finalizarlo
           if(archivo.exists()){
               while((texto=br.readLine())!=null){
                   //Cada linea del archivo al JtextArea
                   txtmostrar.append(texto+"\n");
               }
           }
           
       } catch (IOException ioe) {
           JOptionPane.showMessageDialog(rootPane, "Error al abrir el archivo");
            
        }
    }//GEN-LAST:event_btnabrirActionPerformed

    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
      String ArchivoTexto="cancion.txt";
      try{
          File archivo =new File(directorio+"/"+ArchivoTexto);
          if(!archivo.exists()){
              //BufferedWriter ayuda a manejar los streams
              //FileWriter nos ayuda a escribir dentro del archivo
              
              BufferedWriter bw= new BufferedWriter(new FileWriter(archivo));
              //usamos el metodo .write para escribir la cadena
              bw.write(txtmostrar.getText());
              //Muy importante, siempre cerrar el archivo
              bw.close();
              txtmostrar.setText(""); //Limpiamos elJTextArea 
              JOptionPane.showMessageDialog(rootPane, "Archivo guardado con Ã©xito");
          }
      } catch (IOException e1) {
          JOptionPane.showMessageDialog(rootPane, "Error al guardar en el archivo");
          
        }
    }//GEN-LAST:event_btnguardarActionPerformed

    private void btncambiarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncambiarActionPerformed
        String can;
        can=txtmostrar.getText();  //Obtenemos el texto del JTextArea
        char cadena[]=new char[can.length()];
        txtmostrar.setText("");//Limpiamos el JTextArea
        for (int i = 0; i < cadena.length; i++) {
            //ciclo para ingreso de valores
            cadena[i]= can.charAt(i);
        }
        for (int i = 0; i < cadena.length; i++) {
            //ciclo para evaluar la cadena
            if(cadena[i]=='a')
            {
                cadena[i]='e'; //se asigna la letra e por a
                
            }
        can= ""+ cadena[i];
        txtmostrar.append(can);
        }
    }//GEN-LAST:event_btncambiarActionPerformed

